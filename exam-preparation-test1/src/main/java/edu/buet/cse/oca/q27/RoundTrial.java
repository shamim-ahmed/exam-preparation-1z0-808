package edu.buet.cse.oca.q27;

public class RoundTrial {
  public static void main(String... args) {
    double d1 = -0.12D;
    double d2 = -1.12d;
    double d3 = -1.5;
    double d4 = -1.6;
    double d5 = 0.12;
    double d6 = 0.5;
    double d7 = 0.6;

    System.out.println(Math.round(d1));
    System.out.println(Math.round(d2));
    System.out.println(Math.round(d3));
    System.out.println(Math.round(d4));
    System.out.println();

    System.out.println(Math.round(d5));
    System.out.println(Math.round(d6));
    System.out.println(Math.round(d7));
  }
}
